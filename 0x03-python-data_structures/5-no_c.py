#!/usr/bin/python3
def no_c(my_string):
    """Removes all characters c and C from a string.
    
    Args:
    my_string: The string to be modified.
        
    Returns:
    A new string with the removed characters."""
    new_string = ""
    for char in new_string:
        if char != "C" and char != "c":
            new_string += char
    return (new_string)
